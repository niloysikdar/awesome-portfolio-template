// Mouse Leave (Hover Out)

$(".skill:nth-child(n)").mouseleave(function () {
    $(".skill:nth-child(n)").css({
        transform: "scale(1)",
        filter: "grayscale(0)",
    });
});

// Mouse Enter (Hover In)

// Skill 1 : HTML5
$(".skill:nth-child(1)").mouseenter(function () {
    $(".skill:nth-child(1)").css({
        transform: "scale(1.1)",
        filter: "grayscale(0)",
    });
    $(".skill:not(:nth-child(1))").css({
        transform: "scale(0.8)",
        filter: "grayscale(1)",
    });
});

